   /**
    * CVXGen problem statement:
    *
    * parameters
    * A (wrenchLength, nDoF)
    * b (wrenchLength, 1)
    * C (wrenchLength, wrenchLength) diagonal psd
    *
    * Js (nDoF, nDoF)
    * ps (nDoF, 1)
    * Ws (nDoF, nDoF) diagonal psd
    *
    * Lambda (nDoF, nDoF) diagonal psd
    *
    * Q (wrenchLength, nContacts * nSupportVectors * nPointsPerContact)
    * c (wrenchLength, 1)
    * rhoMin (nSupportVectors * nPointsPerContact * nContacts) positive
    * wRho positive
    *
    * N (nDoF, nNull)
    * z(nNull, 1)
    * end
    *
    * variables
    * rho (nContacts * nSupportVectors * nPointsPerContact)
    * vd (nDoF)
    * end
    *
    * minimize
    * quad(A * vd - b, C) + quad(Js * vd - ps, Ws) + wRho * quad(rho) + quad(vd, Lambda)
    * subject to
    * Q * rho == A * vd + c
    * N' * vd == z
    * rho >= rhoMin
    * end
    * @param momentumOptimizerNativeInput
    */
   public void solve(MomentumOptimizerNativeInput momentumOptimizerNativeInput)
           throws NoConvergenceException
   {
      double[] A = momentumOptimizerNativeInput.getA();
      double[] b = momentumOptimizerNativeInput.getB();
      double[] C = momentumOptimizerNativeInput.getC();
      double[] Js = momentumOptimizerNativeInput.getJs();
      double[] ps = momentumOptimizerNativeInput.getPs();
      double[] Ws = momentumOptimizerNativeInput.getWs();
      double[] Lambda = momentumOptimizerNativeInput.getLambda();
      double[] Q = momentumOptimizerNativeInput.getQ();
      double[] c = momentumOptimizerNativeInput.getc();
      double[] N = momentumOptimizerNativeInput.getN();
      double[] z = momentumOptimizerNativeInput.getZ();
      double[] rhoMin = momentumOptimizerNativeInput.getRhoMin();
      double wRho = momentumOptimizerNativeInput.getwRho();

      solve(A, b, C, Js, ps, Ws, Lambda, Q, c, rhoMin, N, z, wRho);

   }

   public MomentumOptimizerNativeOutput getOutput()
   {
      return momentumOptimizerNativeOutput;
   }

   private void solve(double[] A, double[] b, double[] C, double[] Js, double[] ps, double[] Ws, double[] Lambda, double[] Q,
                                               double[] c, double[] rhoMin,
                                               double[] N, double[] z, double wRho) throws NoConvergenceException
   {
      int numberOfIterations;

      synchronized (solveConch)
      {
         setBufferToArray(ADoubleBuffer, A);
         setBufferToArray(bDoubleBuffer, b);
         setBufferToArray(CDoubleBuffer, C);
         setBufferToArray(JsDoubleBuffer, Js);
         setBufferToArray(psDoubleBuffer, ps);
         setBufferToArray(WsDoubleBuffer, Ws);
         setBufferToArray(LambdaDoubleBuffer, Lambda);
         setBufferToArray(QDoubleBuffer, Q);
         setBufferToArray(cDoubleBuffer, c);
         setBufferToArray(NDoubleBuffer, N);
         setBufferToArray(zDoubleBuffer, z);
         setBufferToArray(rhoMinDoubleBuffer, rhoMin);

         numberOfIterations = solveNative(wRho);

         rhoDoubleBuffer.rewind();
         rhoDoubleBuffer.get(rho);

         vdDoubleBuffer.rewind();
         vdDoubleBuffer.get(vd);

         momentumOptimizerNativeOutput.setJointAccelerations(vd);
         momentumOptimizerNativeOutput.setOptVal(getOptValNative());
         momentumOptimizerNativeOutput.setRho(rho);
         momentumOptimizerNativeOutput.setNumberOfIterations(numberOfIterations);
      }

      if (numberOfIterations < 0)
      {
         throw new NoConvergenceException();
      }
   }

   public static void main(String[] args) throws NoConvergenceException
   {
      double[] A = new double[wrenchLength * nDoF];
      double[] b = new double[wrenchLength];
      double[] C = new double[wrenchLength];    // diagonal
      double[] Js = new double[nDoF * nDoF];
      double[] ps = new double[nDoF];
      double[] Ws = new double[nDoF];    // diagonal
      double[] Lambda = new double[nDoF];    // diagonal
      double[] Q = new double[wrenchLength * nContacts * nSupportVectors * nPointsPerContact];
      double[] c = new double[wrenchLength];
      double[] rhoMin = new double[nSupportVectors * nPointsPerContact * nContacts];
      double[] N = new double[nDoF * nNull];
      double[] z = new double[nNull];
      double[] wRho = new double[1];
      load_default_data(A, b, C, Js, ps, Ws, Lambda, Q, c, rhoMin, N, z, wRho);

      MomentumOptimizerNative momentumOptimizerNative = new MomentumOptimizerNative(nDoF, rhoSize);

      long time = System.nanoTime();
      int nSolves = 10000;
      for (int i = 0; true; i++)
      {
         if (i % nSolves == 0)
         {
            double deltaTimeSeconds = (System.nanoTime() - time) / 1e9;
            double timePerSolveMillis = deltaTimeSeconds / nSolves * 1e3;
            System.out.println("10000 iterations took " + deltaTimeSeconds + " seconds. Time per solve (ms): " + timePerSolveMillis);

//          System.out.println(Arrays.toString(momentumOptimizerNative.getRho()));

            time = System.nanoTime();
         }

         momentumOptimizerNative.solve(A, b, C, Js, ps, Ws, Lambda, Q, c, rhoMin, N, z, wRho[0]);
      }
   }

